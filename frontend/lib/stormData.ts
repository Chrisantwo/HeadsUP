import type { HistoricalStorm, Storm, TrackPoint, StormCategory } from './types'

// ── Wind speed → storm category ──────────────────────────────
function windToCat(ws: number): StormCategory {
  if (ws < 34) return 0
  if (ws < 64) return 1
  if (ws < 96) return 2
  if (ws < 113) return 3
  if (ws < 137) return 4
  return 5
}

// ── Convert HistoricalStorm → Storm (map display) ─────────────
export function convertToStorm(hs: HistoricalStorm): Storm {
  const coords = hs.coordinates
  const n = coords.length
  const peakIdx = Math.floor(n * 0.4)

  // Base date: YYYY-MM-01 00:00, each point = +12h
  const mm = String(hs.month).padStart(2, '0')
  const baseMs = new Date(`${hs.year}-${mm}-01T00:00:00Z`).getTime()

  const path: TrackPoint[] = coords.map(([lat, lon], i) => {
    // Wind/pressure ramp: up to peakIdx then decay
    let frac: number
    if (i <= peakIdx) {
      frac = peakIdx === 0 ? 1 : i / peakIdx
    } else {
      frac = n - 1 === peakIdx ? 0 : 1 - (i - peakIdx) / (n - 1 - peakIdx)
    }
    // Interpolate from 35 kt at start/end to maxWinds at peak
    const minWind = 35
    const ws = Math.round(minWind + frac * (hs.maxWinds - minWind))
    // Pressure inversely mirrors wind
    const maxPressure = 1005
    const pressure = Math.round(maxPressure - frac * (maxPressure - hs.minPressure))

    const timeMs = baseMs + i * 12 * 3600 * 1000
    const d = new Date(timeMs)
    const yr = d.getUTCFullYear()
    const mo = String(d.getUTCMonth() + 1).padStart(2, '0')
    const dy = String(d.getUTCDate()).padStart(2, '0')
    const hr = String(d.getUTCHours()).padStart(2, '0')
    const time = `${yr}-${mo}-${dy} ${hr}:00`

    return { lat, lon, time, windSpeed: ws, pressure, category: windToCat(ws) }
  })

  const peakCategory = path.reduce(
    (best, p) => (p.category > best ? p.category : best),
    0 as StormCategory,
  )

  return { name: hs.name, year: hs.year, path, peakCategory }
}

// ── Lookup helpers ─────────────────────────────────────────────
export function getStormsForYear(year: number): HistoricalStorm[] {
  return STORM_CATALOG.filter(s => s.year === year)
}

